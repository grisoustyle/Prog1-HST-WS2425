'''Die  Gebäudeverwaltung  der  Hochschule  verwaltet  Gebäude,  Räume,  Arbeitsplätze  für 
Mitarbeitende und Stühle der Hochschule. Ein Gebäude hat einen Namen, eine Adresse 
sowie eine Höhe, Breite und Tiefe. Weiterhin hat das Gebäude mehrere Räume, die je-
weils eine Raumnummer haben, sowie eine maximale Kapazität von Personen. Räume 
können  unterschiedliche  Typen  haben  wie  z.B.  Vorlesungssaal,  Büroraum  oder  Lager-
raum. Büroräume können Arbeitsplatz für einen oder mehrere Mitarbeitende sein. Die Adresse 
des Gebäudes besteht aus der Straße, der Postleitzahl, dem Land und der Stadt. An einer 
Adresse können mehrere Gebäude stehen. In einem Raum stehen Stühle. Stühle können 
von unterschiedlichem Typ sein, z.B. Standard-Stuhl, Bürostuhl, Hocker, Sessel, Klapp-
stuhl. In einem Hörsaal dürfen nur Standard-Stühle stehen, in einem Büro dagegen be-
liebige  Stühle.  Wenn  ein  Stuhl  gekauft  (erzeugt)  wird,  soll  er  standardmäßig  zunächst 
ins  Lager  gestellt  werden.  Aus  dem  Lager  kann  er  dann  beliebig  verteilt  werden.  Be-
trachten  Sie  folgende  Integritätsbedingungen:  Wenn  Sie  ein  Gebäude  abreißen,  ver-
schwinden auch die zugehörigen Räume.'''